#!/usr/bin/env python3

def miles_to_km(miles):
    miles = float(miles)
    km = miles*1.60934
    return km

def km_to_miles(km):
    km = float(km)
    miles = km/1.60934
    return km


def kg_to_pounds(kg):
    kg = float(kg)
    pounds = kg*2.2046226218
    return pounds

def pounds_to_kg(pounds):
    pounds = float(pounds)
    kg = pounds/2.2046226218
    return kg
